package ex05;

public class OperEx01 {
    public static void main(String[] args) {
        System.out.println(1+1); // 1+1 = 2 출력
        System.out.println(1-1); // 1-1 = 0 출력
        System.out.println(2*2); // 2*2 = 4 출력
        System.out.println(2/2); // 2/2 = 1 출력
        System.out.println(9%7); // 9을 7로 나눴을 때 남는 값 출력

        // 더하기 (산술더하기 or 결합)
        // 문자 + 숫자이기 때문에 각각 1 1로 출력
        System.out.println("1"+1);
    }
}